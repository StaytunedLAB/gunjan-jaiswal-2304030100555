// string.js
"use strict";

let greeting = "Hello, Gunjan!";
console.log("String:", greeting);
console.log("Length:", greeting.length);
console.log("Uppercase:", greeting.toUpperCase());
console.log("Lowercase:", greeting.toLowerCase());
console.log("Slice:", greeting.slice(0, 5));
console.log("Replace:", greeting.replace("Gunjan", "Friend"));
console.log("Includes 'Hello'?", greeting.includes("Hello"));